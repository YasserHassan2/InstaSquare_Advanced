    public static Bitmap mBitmapAssets(Context context, String string) {
        InputStream inputStream;
        try {
            inputStream = context.getAssets().open(string);
            Bitmap decodeStream = BitmapFactory.decodeStream(inputStream);
            inputStream.close();
            return decodeStream;
        } catch (Exception e) {
            return null;
        }
    }


